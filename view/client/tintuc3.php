        <p>
            Tại TPHCM, nơi có xấp xỉ 20.000 DN xây dựng và VLXD. Từ đầu năm đến nay, số DN làm ăn có lãi chiếm chưa đến 10% trong tổng số DN kể trên. Chuyện thua lỗ, nợ nần trở nên phổ biến tại hầu hết DN thuộc lĩnh vực này. Ông Trương Phú Cường, Chủ tịch Hiệp hội Xây dựng và VLXD TPHCM, cho biết 4-5 năm nay, bất động sản đóng băng kéo theo ngành xây dựng và VLXD tuột dốc. Bất động sản đóng băng, chủ đầu tư không có tiền trả cho nhà thầu chính, nhà thầu chính không có tiền trả cho nhà thầu phụ…, nhà cung cấp VLXD là đối tượng được trả tiền cuối cùng. Nhìn chung, DN thép, xi măng, kính xây dựng, gạch ceramic… tồn kho nhiều. Không những vậy, khi bán được hàng thì rất khó thu hồi vốn vì chủ đầu tư không có tiền trả. Đặc biệt, tình trạng công nợ giữa chủ đầu tư và DN xây dựng, VLXD đang ở mức báo động, nhiều rủi ro. “Không có tiền, khó tiếp cận vốn vay, không có đầu ra buộc DN phải thu hẹp sản xuất và “sức khỏe” yếu dần, một số DN nhỏ và vừa phải ngưng hoạt động. Đó là do thủ tục phá sản phức tạp, nếu việc phá sản dễ dàng thì các DN không chỉ dừng lại ở ngưng hoạt động. Một số DN vẫn đang cố gắng xây dựng và khẳng định thương hiệu, trong đó có thể kể đến Coteccons, Cofico, Hòa Bình, Eci Saigon, Unicons, Phú Hưng Gia… Tuy nhiên, con số này quá ít, hiện chỉ khoảng 5% trong tổng số 56.000 DN xây dựng và VLXD trên cả nước thật sự mạnh” - ông Trương Phú Cường cho biết.<br>
            <b>Khó khăn chồng chất</b><br>
            Một số DN VLXD cho biết tình trạng cung vượt cầu, tồn kho dư thừa lớn trong ngành một phần do sức mua thị trường yếu kéo dài nhiều năm, một phần do nguyên nhân chủ quan trong ngành. Thời bất động sản còn siêu lợi nhuận, DN đổ xô vào kinh doanh VLXD và sử dụng nguồn vốn vay dồn hết cho lĩnh vực này. Có những DN vay vốn gấp 5-10 lần vốn chủ sở hữu. Nay thị trường đi xuống, tồn kho gia tăng, lãi suất ngân hàng cao… trong khi các DN VLXD với quy mô đầu tư nhỏ, phân tán, khó chuyên nghiệp hóa cao trong sản xuất, vận hành nên không tự chủ được và không có khả năng trả nợ. Thực trạng này kéo dài từ năm 2008-2009 đến nay, nhiều DN đã “chết” là vì vậy.<br>
            Ngoài những lý do trên, phải kể đến sự “lấn sân” của các loại VLXD có nguồn gốc từ Trung Quốc. Do mẫu mã hàng Trung Quốc đa dạng, giá rẻ, nhiều DN Việt Nam đã sang nước này đặt hàng đem về tiêu thụ tại Việt Nam, cạnh tranh gay gắt với hàng cùng chủng loại sản xuất trong nước.<br>
            Ông Nguyễn Vui, Chủ tịch HĐQT kiêm Tổng Giám đốc Công ty Trang trí đá Vĩnh Cửu, cho rằng ngành đá trang trí thuộc dạng “không đụng hàng” tại Việt Nam nhưng 2-3 năm nay, Vĩnh Cửu đã phải giảm 50% sản lượng sản xuất cho nội địa và áp dụng giảm giá, tăng chiết khấu, giảm tiền vận chuyển… để bán hàng. Dự báo năm 2013 còn tiếp tục khó khăn và ngành VLXD sẽ càng thê thảm hơn.<br>
