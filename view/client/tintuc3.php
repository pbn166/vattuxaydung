        <p>
            <b>Đầu ra thu hẹp, bị chiếm dụng vốn, hàng ế kéo dài... nên các doanh nghiệp ngành xây dựng và vật liệu xây dựng đều lao đao.</b> <br>
            Từ năm 2012 đến nay, ngành vật liệu xây dựng (VLXD) luôn nằm trong tốp đầu chỉ số tồn kho hàng hóa. Trong đó, xi măng, thép là 2 mặt hàng có mức tồn kho cao nhất.<br>
            <b>Hầu hết thua lỗ</b><br>
            Tại thời điểm 1-4, tồn kho trong sản xuất sắt, thép, gang tăng 10,9%. Đến đầu quý II/2013, tổng sản lượng xi măng tồn kho cả nước tăng hơn 25% so với cùng kỳ năm 2012. Sức tiêu thụ trên thị trường tiếp tục suy giảm, các nhà máy cắt giảm công suất hoạt động để hạn chế nguồn cung. Theo báo cáo tài chính quý II/2013, một số doanh nghiệp (DN) xi măng như Công ty CP Xi măng Hà Tiên 1, Xi măng Bỉm Sơn, Xi măng Vicem Hoàng Mai giảm lợi nhuận hoặc lỗ. Vấn đề nan giải của ngành xi măng hiện nay là cung vượt cầu, nhiều DN phải tìm đường xuất khẩu. Kế đến là mặt hàng thép. Kể từ đầu năm 2013 đến nay, ngành thép phải đối mặt với tình trạng giảm sản lượng, tăng tồn kho: Chỉ số tồn kho tăng gấp 3 lần so với mức giảm nguồn cung ứng.<br>
        </p>
