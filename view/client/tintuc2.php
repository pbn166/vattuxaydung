        <p>
            Hàng tồn kho mặc dù đã giảm dần trong thời gian qua nhưng cũng kéo theo sự suy giảm đáng lo ngại về chỉ số sản xuất của doanh nghiệp, đồng thời cho thấy doanh nghiệp trong nước có thể bị đánh bại bất cứ lúc nào.<BR>
            <b>Giảm vẫn lo</b><br>
            Ông Trương Phú Cường, Chủ tịch Hiệp hội Vật liệu và Xây dựng TP.HCM cho rằng, riêng về ngành vật liệu xây dựng, câu chuyện giảm hàng tồn kho trong thời gian vừa qua không phải là điều đáng mừng mà là điều rất đáng lo ngại.<br>
            Theo ông Cường, để giảm hàng tồn kho, doanh nghiệp (DN) buộc phải thu hẹp sản xuất và một số DN buộc phải chấp nhận giảm hàng tồn kho bằng cách xuất khẩu với giá thành thấp, thậm chí là lỗ để thu hồi được đồng nào hay đồng nấy.<br>
            “Hiện đang xảy ra tình trạng mất cân đối giữa cung và cung do năng lực sản xuất của ngành vật liệu so với nhu cầu tiêu thụ của thị trường vượt từ 20 – 30%. Lý do là qua một thời gian dài (khoảng 5 năm) thị trường bất động sản đóng băng khiến cho ngành vật liệu xây dựng không thể tiêu thụ được”, ông Cường chia sẻ.
        </p>
