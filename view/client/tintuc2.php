        <p>
            Ông Cường cho biết, lượng tồn kho đang tập trung nhiều nhất ở sắt thép, xi măng, gốm sứ, gạch xây dựng, thiết bị vệ sinh… Đây là những sản phẩm có liên quan mật thiết với lĩnh vực bất động sản.<br>
            Chuyên gia kinh tế, TS. Đinh Thế Hiển cho rằng, ngành thép đã được cảnh báo từ rất sớm ngay từ chuyện thừa công suất, giá thành, chất lượng, mẫu mã…<br>
            “Nói đến sắt thép trước tiên phải nói đến tính quy mô, DN muốn bước ra thị trường phải hiểu được điều này; thứ hai là giá thành, thứ ba là chất lượng, đây là những điều kiện để ngành thép trong nước cạnh tranh và tồn tại”, ông Hiển nhấn mạnh khi có ý kiến cho rằng ngành thép tồn kho là do thép ngoại tràn vào.<br>
            Trong khi đó, ông Trương Phú Cường cũng thừa nhận rằng, trong một thời gian dài các DN trong nước đầu tư cho ngành thép rất nhiều nhưng chỉ dựa trên số liệu dự báo tăng trưởng sắt thép hàng năm. Đây cũng là một thực trạng cho thấy ngành thép phát triển một cách ồ ạt, không có tính định hướng.<br>
            Còn đối với ngành da giày, ông Nguyễn Văn Khánh, Chủ tịch Hội Da giày quận 4 (TP.HCM) than thở: “Làm ăn mà cái vốn không xoay vòng được thì rất khó khăn, tồn kho có nghĩa là không bán được, không bán được thì công nhân bỏ nghề, trong khi doanh nghiệp làm gì thì làm cũng phải tìm cách giữ lao động lại để sản xuất…”<br>
            Là một trong những ngành kinh tế mũi nhọn, nhưng da giày nội địa đang đối diện với rất nhiều khó khăn. Theo ghi nhận của PV, tình trạng ế ẩm, vắng khách xảy ra ở hầu hết các hiệu giày trên địa bàn TP, từ trung tâm thương mại sang trọng đến các cửa hàng nhỏ lẻ.<br>
            <b>Đằng sau câu chuyện hàng tồn</b><br>
            Theo số liệu từ Bộ Công Thương, tính đến tháng 6 vừa qua, chỉ số hàng tồn kho đã giảm chỉ còn 11,8%. Tuy nhiên, chỉ số sản xuất của một số ngành lại giảm mạnh hơn. Cụ thể, sản xuất xi măng đã giảm 38,4%, dệt giảm 31,8%, giày dép giảm 25,7%...<br>
            Chuyên gia kinh tế, TS. Đinh Thế Hiển cho rằng, bản chất của câu chuyện giảm (hàng tồn kho) này không chỉ dựa trên việc doanh nghiệp đẩy mạnh bán hàng tồn kho mà còn là do doanh nghiệp suy giảm sản xuất kéo theo hàng tồn kho giảm theo.  Theo TS. Hiển, đây là vấn đề rất đáng lo ngại.<br>
            Bên cạnh đó, trước sự tấn công ồ ạt của hàng hóa nước ngoài mà nổi bật gần đây là thép Trung Quốc, cho thấy DN trong nước rất yếu ớt và có thể bị đối thủ hạ “đo ván” bất cứ lúc nào.<br>
            Theo các chuyên gia, kể từ khi Việt Nam gia nhập WTO, năng lực cạnh tranh và khả năng sống còn của DN trong nước đã bắt đầu lộ diện. Hàng trăm ngàn DN phải ngừng hoạt động, giải thể, phá sản. Riêng 6 tháng đầu năm đã có gần 25.000 DN ngừng hoạt động. Hàng ngàn DN khác tuy chưa giải thể nhưng cũng èo uột, xương xẩu.<br>
            Nhiều nhà quan sát cũng cho rằng, không phải ngẫu nhiên mà có sự mua bán, sáp nhập rầm rộ trong thời gian qua. Từ chuyện “bán mình” của Highlands Coffee đến thương vụ chia sẻ thương hiệu và cổ tức với Lotte của Bibica – nhãn hàng bánh kẹo trong nước đã cất công xây dựng thương hiệu hàng chục năm trời.<br>
            Bên cạnh đó, dường như lợi dụng chiến thuật “đánh địch lúc địch suy yếu”, nhiều Tập đoàn bán lẻ nước ngoài tranh thủ nhảy vào Việt Nam nhằm chia “miếng bánh ngọt” của thị trường gần 90 triệu dân này. Đơn cử như trường hợp của Starbucks và sắp tới có thể là McDonald’s.<br>
            Chuyện bị lấn lướt ngay trên sân nhà không còn là chuyện lạ đối với nhiều DN trong nước. Các chuyên gia cho rằng, đã đến lúc DN phải cơ cấu lại toàn bộ hệ thống. Ông Trương Phú Cường nhận định, bên cạnh hiểm họa cũng là cơ hội để DN làm điều này.<br>
            Bình luận trước thông tin UBND TP.HCM đã giao các cơ quan có liên quan tham mưu cho đề án tái cơ cấu DN, mà bắt đầu từ DN Nhà nước, sau đó nhân rộng mô hình cho các DN còn lại, nhiều chuyên gia cho rằng, đây là chủ trương rất đúng đắn. Tuy nhiên, vấn đề tái như thế nào vẫn còn là bài toán đang đặt ra.<br>
